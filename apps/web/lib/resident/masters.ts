export const RESIDENCE_YEAR_OPTIONS = [
  { value: '<1', label: '1 年未満（来たばかり）' },
  { value: '1-2', label: '1〜2 年' },
  { value: '3-5', label: '3〜5 年' },
  { value: '6-10', label: '6〜10 年' },
  { value: '11-15', label: '11〜15 年' },
  { value: '16-20', label: '16〜20 年' },
  { value: '20+', label: '20 年以上' },
] as const;
export type ResidenceYearBucket = (typeof RESIDENCE_YEAR_OPTIONS)[number]['value'];

/**
 * 在住年数バケットから「来た年」の中央値を推定する。
 * users.arrival_year に保存する数値を作るときに使う。
 */
export function arrivalYearFromBucket(bucket: ResidenceYearBucket): number {
  const thisYear = new Date().getFullYear();
  switch (bucket) {
    case '<1':
      return thisYear;
    case '1-2':
      return thisYear - 1;
    case '3-5':
      return thisYear - 4;
    case '6-10':
      return thisYear - 8;
    case '11-15':
      return thisYear - 13;
    case '16-20':
      return thisYear - 18;
    case '20+':
      return thisYear - 22;
  }
}

/**
 * users.arrival_year → 表示用のラベル（"在住 5 年" 等）
 */
export function residenceYearsLabel(arrivalYear: number | null): string | null {
  if (arrivalYear === null || !Number.isFinite(arrivalYear)) return null;
  const years = Math.max(0, new Date().getFullYear() - arrivalYear);
  if (years < 1) return '在住 1 年未満';
  if (years === 1) return '在住 1 年';
  return `在住 ${years} 年`;
}
